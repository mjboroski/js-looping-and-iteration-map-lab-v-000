function lowerCaseDrivers (list) {
  return list.map(function (driver) {
    return driver.toLowerCase();
  });
}

function nameToAttributes (list) {
  return list.map(function (driver) {
    return driver.toLowerCase();
  });
}

function attributesToPhrase (list) {
  return list.map(function (driver) {
    return driver.toLowerCase();
  });
}
